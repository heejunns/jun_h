# 2랑 나눠서 나머지가 0이면 짝수고 1이면 홀수로 13이 홀수인지 판별한다. 
# 예를 들어서 
print(2%2) # 나머지가 0 으로 출력 되니까 짝수이다. 
print(3%2) # 나머지가 1로 출력 되니까 홀수이다. 
# 그럼 13은 ? 
print(13%2) # 예상했던 것처럼 나머지가 1로 출력 된다. 13은 홀수이다.
# if 문을 사용한 프로그램  
number = 13
b = number%2 
if b==1 : 
    print("자연수" +str(number)+"는 홀수 입니다.")
else : 
    print("자연수"+str(number)+"는 짝수 입니다.")



