import sys 
a,b = map(int,sys.stdin.readline().split())
x = []
y = []
for i in str(a):
    x.insert(0,i)
for l in str(b):
    y.insert(0,l)
x1 = ""
y1 = ""
for i in x:
    x1+=i
for l in y:
    y1+=l
q = []
for k in str(int(x1)+int(y1)):
    q.insert(0,k)
q1 = ""
for i in q:
    q1+=i
print(int(q1))
