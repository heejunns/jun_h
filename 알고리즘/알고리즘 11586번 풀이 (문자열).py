a = int(input())
x = 1
y = []
z = []
while x<=a:
    face = input()
    for l in face:
        z.append(l)
    y.append(z)
    z = []
    x+=1
b = int(input())
for i in y:
    if b == 1:
        print("".join(i))
    elif b ==2:
        i.reverse()
        print("".join(i))
if b == 3:
    y.reverse()
    for i in y:
        print("".join(i))
        
